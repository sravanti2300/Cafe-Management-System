    private void ShowProducts() {

        try {
            Con = DriverManager.getConnection(Constants.dbUrl, Constants.dbUser, Constants.dbPwd);
            St = Con.createStatement();
            Rs = St.executeQuery("select * from ProductTbl");
            ItemsList.setModel(DbUtils.resultSetToTableModel(Rs));

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    int BNum;

    private void CountBill() {

        try {

            St1 = Con.createStatement();
            Rs1 = St1.executeQuery("select Max(BNum) from BillTbl");
            Rs1.next();
            BNum = Rs1.getInt(1) + 1;

        } catch (SQLException e) {
        }
    }

    private void InsertBill() {

        try {
            CountBill();
            Con = DriverManager.getConnection(Constants.dbUrl, Constants.dbUser, Constants.dbPwd);
            PreparedStatement Pst = Con.prepareStatement("insert into BillTbl value(?,?,?,?)");
            Pst.setInt(1, BNum);
            Pst.setString(2, SellerTb.getText());
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            Pst.setString(3, now.format(dtf));
            Pst.setInt(4, grandTotal); // suggested to use Integer.valueof
            int row = Pst.executeUpdate();
            // JOptionPane.showMessageDialog(this, "Bill Added!");
            Con.close();
            ShowProducts();
        } catch (SQLException ex) {
            Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        }

    }

