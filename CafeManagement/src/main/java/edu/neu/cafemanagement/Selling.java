    int grandTotal = 0;

    private void AbbBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_AbbBtnActionPerformed
        // Add to Bill Code

        if (PrNameTb.getText().isEmpty() || PrQtyTb.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information!");
        } else {
            int Total = Integer.valueOf(PriceTb.getText()) * Integer.valueOf(PrQtyTb.getText());
            grandTotal = grandTotal + Total;
            TotalLb.setText(" $ " + grandTotal);
            DefaultTableModel model = (DefaultTableModel) BillTable.getModel();
            String nextRowId = Integer.toString(model.getRowCount());
            model.addRow(new Object[] {
                    Integer.parseInt(nextRowId) + 1,
                    PrNameTb.getText(),
                    PriceTb.getText(),
                    PrQtyTb.getText(),
                    Total
            });
        }

    }// GEN-LAST:event_AbbBtnActionPerformed

